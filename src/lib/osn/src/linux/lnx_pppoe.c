#define LNX_PPPOE_PPP_DIR       "/tmp/ppp"
#define LNX_PPPOE_PEERS_DIR     "/tmp/ppp/peers"
#define LNX_PPPOE_PAP_SECRETS   "/tmp/ppp/pap-secrets"
#define LNX_PPPOE_CHAP_SECRETS  "/tmp/ppp/chap-secrets"

